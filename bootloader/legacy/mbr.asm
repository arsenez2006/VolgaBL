cpu 386
bits 16
global __start
; -------------------------------------------------------------------------------------------------
; MBR ENTRY
; -------------------------------------------------------------------------------------------------
section .text
__start:
    mov ax, 0x07C0
    mov ds, ax

    mov si, msg

.loop:
    lodsb
    or al, al
    jz .halt
    mov ah, 0x0E
    int 0x10
    jmp .loop
.halt:
    hlt
    jmp short .halt

; -------------------------------------------------------------------------------------------------
; DATA
; -------------------------------------------------------------------------------------------------
section .data
msg db"Hello, world!",0
